<?php

namespace App\Http\Resources\Employees\Pagination;

use App\Http\Resources\Employees\Base\EmployeesResources;
use Illuminate\Http\Resources\Json\ResourceCollection;

class EmployeesPaginationResourceCollection extends ResourceCollection
{
    /**
     * Transform the resource collection into an array.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return array
     */
    public function toArray($request)
    {
        return [
            'employees' => EmployeesResources::collection($this->collection),
            'meta' => [
                'total' => $this->total(),
                'count' => $this->count(),
                'per_page' => $this->perPage(),
                'current_page' => $this->currentPage(),
                'total_pages' => $this->lastPage()
            ]
        ];
    }
}
